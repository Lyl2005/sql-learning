Запросом UPDATE можно обновлять значения нескольких столбцов одновременно. В этом случае простейший запрос будет выглядеть так:

UPDATE таблица SET поле1 = выражение1, поле2 = выражение2
На складе, кроме хранения и получения книг, выполняется их оптовая продажа.
Для реализации этого действия включим дополнительный столбец buy в таблицу book: drawing

Пример В столбце buy покупатель указывает количество книг, которые он хочет приобрести.
Для каждой книги, выбранной покупателем, необходимо уменьшить ее количество на складе на указанное в столбце buy количество,
а в столбец buy занести 0.
UPDATE book 
SET amount = amount - buy,
    buy = 0;

SELECT * FROM book;

